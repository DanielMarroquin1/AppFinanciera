    }
    if (lang == 'en') return 'Day $day1 of every month';
    if (lang == 'pt') return 'Dia $day1 de cada mês';
    if (lang == 'fr') return 'Jour $day1 de chaque mois';
    if (lang == 'it') return 'Giorno $day1 di ogni mese';
    return 'Día $day1 de cada mes';
  }

